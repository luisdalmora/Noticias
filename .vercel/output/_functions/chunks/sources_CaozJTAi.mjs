function getGoogleNewsUrl(query, country = "BR", language = "pt-BR") {
  const encodedQuery = encodeURIComponent(query);

  const localeMap = {
    BR: {
      hl: "pt-BR",
      gl: "BR",
      ceid: "BR:pt-419",
    },
    US: {
      hl: "en-US",
      gl: "US",
      ceid: "US:en",
    },
    GLOBAL: {
      hl: "en-US",
      gl: "US",
      ceid: "US:en",
    },
  };

  const locale = localeMap[country] || localeMap.BR;

  return `https://news.google.com/rss/search?q=${encodedQuery}&hl=${locale.hl}&gl=${locale.gl}&ceid=${locale.ceid}`;
}

